									<h3>Antecedentes</h3>
                                    	<span class="image featured"><img src="images/proyecto_dk1.jpg" alt="" /></span>
                                        <p>El 14 de febrero de 2010 un grupo de ec&oacute;logos y bot&aacute;nicos que  incluyeron a Jos&eacute; Sarukh&aacute;n (CONABIO), Paul Ehrlich y Ann Ehrlich (Stanford  University), Peter Raven (Missouri Botanical Garden), Cristian Samper  (Smithsonian Institution), Roberto Arreola y Carlos Galindo Leal (CONABIO),  visitaron la Quinta de Diana Kennedy. De esta visita naci&oacute; la idea de realizar  una documentaci&oacute;n del conocimiento sobre la diversidad de ingredientes nativos  colectada por Diana Kennedy, a trav&eacute;s de sus libros de cocina mexicana, sus  archivos, notas, fotograf&iacute;as y su huerto.</p>
                                        <p>El 24 de noviembre de 2010 se firm&oacute; el convenio con  el investigador Robert Bye Boettler, con la participaci&oacute;n de Edelmira Linares y  Clarisa Jim&eacute;nez del Instituto de Biolog&iacute;a de la Universidad Nacional Aut&oacute;noma  de M&eacute;xico, sobre el proyecto titulado <strong>&ldquo;Documentaci&oacute;n de la  biodiversidad de la gastronom&iacute;a mexicana: rescate de los archivos culinarios de  Diana Kennedy&rdquo;</strong> que se desarroll&oacute; en dos fases.</p>
                                    
                                    
							  		<h3>Objetivos</h3>
                                    	<span class="image featured"><img src="images/proyecto_dk2.jpg" alt="" /></span>
                                    	<p>El objetivo del proyecto fue documentar y sistematizar el archivo  culinario de Diana Kennedy incluyendo sus notas in&eacute;ditas, fotograf&iacute;as, muestras  de plantas que ella ha recolectado (secas y vivas) y sus comentarios sobre las  mismas.</p>
                                        <p>El proyecto tambi&eacute;n analiz&oacute; los ingredientes de cuatro libros de Diana Kennedy: <strong>Oaxaca al Gusto (2008), M&eacute;xico una odisea culinaria (2001), Cocina  esencial de M&eacute;xico (2001)</strong> y <strong>El arte  de la cocina mexicana (1994)</strong>. Se digitalizaron adem&aacute;s sus notas  de viajes realizados en M&eacute;xico y sus diapositivas. Se obtuvieron espec&iacute;menes  bot&aacute;nicos del Huerto para identificaci&oacute;n taxon&oacute;mica, as&iacute; como su documentaci&oacute;n  fotogr&aacute;fica. </p>
                                        <p>Se realizaron visitas peri&oacute;dicas para  entrevistar a Diana y se efectuaron salidas con Diana a mercados de Chilapa, Guerrero,  Santiago de Anaya, Hidalgo, Ciudad de Oaxaca y Tlacolula, Oaxaca, Huauchinango  y Xicotepec, Puebla y San Andr&eacute;s Tuxtla, Veracruz.</p>
                              
                                    
                                    <h3>Resultados</h3>
                                     <span class="image featured"><img src="images/proyecto_dk3.jpg" alt="" /></span>
                                     <p><strong>Ingredientes aut&oacute;ctonos</strong></p>

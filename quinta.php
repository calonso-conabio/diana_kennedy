									  <p>En 1976  Diana adquiri&oacute; una propiedad de 3 hect&aacute;reas que ahora conforman <strong>&ldquo;La Quinta  Diana&rdquo;</strong>. La Quinta Diana est&aacute; ubicada en el pueblo de San Francisco Coatepec de  Morelos, tambi&eacute;n conocido como &ldquo;San Pancho&rdquo; al suroeste de Zit&aacute;cuaro en el  estado de Michoac&aacute;n. En el centro del poblado se ubica la iglesia de San  Francisco de As&iacute;s, primer templo franciscano de la regi&oacute;n de los a&ntilde;os 1535 a  1550.</p>
                                        <p>En la Quinta  est&aacute; su casa ecol&oacute;gica, con uso racional de agua, luz y uso eficiente de la  energ&iacute;a del sol. El huerto de la Quinta est&aacute; divido en dos secciones, una  secci&oacute;n con la hortaliza y el &ldquo;kanch&eacute;&rdquo; y otra con plantas espont&aacute;neas. Adem&aacute;s  hay un huerto interior con plantas de ornato y plantas comestibles y un  invernadero de 60 m2 con plantas de regiones c&aacute;lidas (Yucat&aacute;n,  Campeche, Puebla y Oaxaca, entre otras).</p>
                                        <p>En la Quinta  Diana se documentaron alrededor de <strong>200</strong> especies de plantas. Algunas tienen uso comestible, como condimentos, forraje,  medicinales, elaboraci&oacute;n de herramientas, para cercos vivos y de ornato.  Tambi&eacute;n se incluyen un gran n&uacute;mero de hierbas, arbustos y &aacute;rboles silvestres.</p>
                                        
                                        
